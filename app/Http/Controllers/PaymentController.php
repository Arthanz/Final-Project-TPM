     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validated = $request->validate([
            'paymentProof'=>'required|mimes:jpg,png,jpeg'
        ]);

        $extension = $request->file('paymentProof')->getClientOriginalExtension();
        // $filename = $request->id.'_'.$request->paymentProof.'.'.$extension;
        $filename = $request->file('paymentProof')->getClientOriginalName();
        $request->file('paymentProof')->storeAs('/public/storage/payment', $filename);


        payment::create([
            'paymentProof'=>$filename
        ]);

        return redirect('/paymentVerif');
    }

